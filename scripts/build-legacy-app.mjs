import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const sourceFiles = [
  'js/renderer.js',
  'js/auth.js',
  'js/game.js'
];

const outputFile = path.join(rootDir, 'js', 'legacy-app.generated.js');

async function buildLegacyBundle() {
  const compatBindings = [
    'BACKEND_URL',
    'WC_PROJECT_ID',
    'CONFIG',
    'BONUS_TYPES',
    'isMobile',
    'RequestError',
    'request',
    'isAuthenticated',
    'getAuthIdentifier',
    'updateWalletUI',
    'signMessage',
    'loadAndDisplayLeaderboard',
    'saveResultToLeaderboard',
    'escapeHtml',
    'sanitizeTelegramHandle',
    'AssetManager',
    'assetManager',
    'DOM',
    'ctx',
    'gameState',
    'player',
    'curves',
    'obstacles',
    'bonuses',
    'coins',
    'spinTargets',
    'playerUpgrades',
    'playerEffects',
    'playerBalance',
    'playerRides',
    'inputQueue',
    'audioManager',
    'audioSettings',
    'setSfxEnabled',
    'setMusicEnabled',
    'toggleSfxMute',
    'toggleMusicMute',
    'syncAllAudioUI',
    'showBonusText',
    'showStore',
    'hideStore',
    'updateUI',
    'showLeaderboardSkeletons',
    'displayLeaderboard',
    'loadPlayerRides',
    'useRide',
    'updateRidesDisplay',
    'applyStoreDefaultLockState',
    'loadPlayerUpgrades',
    'updateStoreUI',
    'buyUpgrade',
    'showRules',
    'hideRules',
    'updateRulesAudioButtons',
    'resetGameSessionState',
    'update',
    'applyBonus',
    'collectCoin',
    'initAudioToggles',
    'restoreAudioSettings',
    'particlePool',
    'spawnParticles',
    'updateParticles',
    'drawParticles',
    'PerformanceMonitor',
    'perfMonitor',
    'WC'
  ];

  const chunks = [
    '/* Auto-generated file. DO NOT EDIT.\n',
    ' * Generated by: node scripts/build-legacy-app.mjs\n',
    ' */\n\n',
    `const { ${compatBindings.join(', ')} } = window;\n`,
    'let { laneCooldown, bestScore, bestDistance, web3, userWallet, isWalletConnected, authMode, primaryId, telegramUser, linkedTelegramId, linkedTelegramUsername, linkedWallet } = window;\n\n'
  ];

  for (const relFile of sourceFiles) {
    const absoluteFile = path.join(rootDir, relFile);
    const content = await readFile(absoluteFile, 'utf8');
    chunks.push(`/* ===== BEGIN ${relFile} ===== */\n`);
    chunks.push(content.endsWith('\n') ? content : `${content}\n`);
    chunks.push(`/* ===== END ${relFile} ===== */\n\n`);
  }

  await mkdir(path.dirname(outputFile), { recursive: true });
  await writeFile(outputFile, chunks.join(''), 'utf8');
  console.log(`✅ Generated ${path.relative(rootDir, outputFile)}`);
}

buildLegacyBundle().catch((error) => {
  console.error('❌ Failed to generate legacy bundle:', error);
  process.exitCode = 1;
});
